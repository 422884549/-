	public void addlisten(final JButton jb, final int x, final int y) {
		if (seat.length != 0) {
			int closize = seat.length;
			int rowsize = seat[0].length;
			jb.setEnabled(true);
			jb.setVisible(true);
			if (jb.getActionListeners().length != 0) {
				jb.removeActionListener(jb.getActionListeners()[0]);
			}
			jb.setBackground(Color.WHITE);
			if (x >= closize || y >= rowsize) {
				jb.setVisible(false);
			} else {
				if (seat[x][y] == 1) {
					System.out.println("aaa" + x + " " + y);
					jb.setEnabled(false);
					jb.setBackground(new Color(255, 204, 0));
				}
				jb.addActionListener(new ActionListener() {

					@Override
					public void actionPerformed(ActionEvent e) {
						// TODO Auto-generated method stub
						int a = bilibili++;
						if (jb.getBackground().equals(Color.WHITE)) {

							int i = 0;
							while (true) {
								System.out.println(a);
								if (i == 5) {
									System.out.println("");
									JOptionPane.showMessageDialog(null,
											"");
									break;
								}
								if (waitsize[i][0] == 0) {
									waitsize[i][0] = 1;
									waitsize[i][1] = x;
									waitsize[i][2] = y;
									System.out.println("," + (4 - i)
											+ "");
									JOptionPane.showMessageDialog(null,
											"," + (4 - i) + "");
									jb.setBackground(new Color(255, 204, 0));
									break;
								} else {
									i++;
								}

							}
						} else {
							if (jb.getBackground().equals(
									new Color(255, 204, 0))) {
								int i = 4;
								while (true) {
									if (i == -1) {
										JOptionPane.showMessageDialog(null,
												"");
										break;
									}
									if (waitsize[i][0] == 1) {
										waitsize[i][0] = 0;
										waitsize[i][1] = 0;
										waitsize[i][2] = 0;
										JOptionPane.showMessageDialog(null,
												"");
										System.out.println("");
										jb.setBackground(Color.white);
										break;
									} else {
										i--;
									}
								}
							}
						}
					}
				});
			}
		}
	}

